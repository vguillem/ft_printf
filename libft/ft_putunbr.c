#include "libft.h"

void	ft_putunbr(unsigned long int n)
{
	if (n != 0)
	{
		while (n > 9)
		{
			ft_putunbr(n / 10);
			n = n % 10;
		}
	}
	ft_putchar(n % 10 + '0');
}
